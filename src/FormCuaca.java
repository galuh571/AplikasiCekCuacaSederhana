    private void btnCekCuacaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekCuacaActionPerformed
        try{
            String kota = txtKota.getText().trim();
            String apiKey = "da601b407b3a1fd34b77254f52af3e21";
            String urlStr = "https://api.openweathermap.org/data/2.5/weather?q=" + kota + "&appid=" + apiKey + "&units=metric";
             
             URL url = new URL(urlStr);
             HttpURLConnection conn = (HttpURLConnection) url.openConnection();
             conn.setRequestMethod("GET");
             
             BufferedReader in = new BufferedReader(new InputStreamReader(conn.getInputStream()));
             StringBuilder response = new StringBuilder();
             String line;
             while ((line = in.readLine()) != null){
                 response.append(line);
             }
             in.close();
             
             JSONObject obj = new JSONObject(response.toString());
             double suhu = obj.getJSONObject("main").getDouble("temp");
             String kondisi = obj.getJSONArray("weather").getJSONObject(0).getString("main");
             
             lblHasil.setText("Kota: " + kota + " | Suhu: " + suhu + "Â°C | Cuaca: " + kondisi);
             
             if (kondisi.toLowerCase().contains("rain")){
                 lblGambar.setIcon(new ImageIcon("src/img/hujan.png"));
             }else if (kondisi.toLowerCase().contains("cloud")){
                 lblGambar.setIcon(new ImageIcon("src/img/berawan.png"));
             }else{
                 lblGambar.setIcon(new ImageIcon("src/img/cerah.png"));
             }
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Kota tidak ditemukan atau gagal mengambil data!");
        }
    }//GEN-LAST:event_btnCekCuacaActionPerformed

    private void btnSimpanFavoritActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanFavoritActionPerformed
        String kota = txtKota.getText().trim();
        if (!kota.isEmpty()){
            cmbFavorit.addItem(kota);
            JOptionPane.showMessageDialog(this, "Kota " + kota + " ditambahkan ke favorit!");
        }
    }//GEN-LAST:event_btnSimpanFavoritActionPerformed

    private void btnSimpanDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanDataActionPerformed
        try (FileWriter fw = new FileWriter("data_cuaca.csv", true)){
            fw.append(lblHasil.getText() + "\n");
            JOptionPane.showMessageDialog(this, "Data cuaca disimpan ke data_cuaca.csv");
        }catch (IOException e){
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data!");
        }
    }//GEN-LAST:event_btnSimpanDataActionPerformed

    private void btnMuatDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMuatDataActionPerformed
       try (BufferedReader br = new BufferedReader(new FileReader("data_cuaca.csv"))){
           String line;
           StringBuilder hasil = new StringBuilder();
           while ((line = br.readLine()) != null) {
               hasil.append(line).append("\n");
           }
           JOptionPane.showMessageDialog(this, hasil.toString());
       }catch (IOException e) {
           JOptionPane.showMessageDialog(this, "Belum ada data tersimpan!");
       }
    }//GEN-LAST:event_btnMuatDataActionPerformed

